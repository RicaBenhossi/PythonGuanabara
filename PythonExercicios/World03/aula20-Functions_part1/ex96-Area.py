def area(largura, comprimento):
    print(f'A área total do terreno com medidas {largura} de largura e {comprimento} de comprimento é {largura * comprimento} m².')


largura = float(input('Informe a largura do terreno (em metros): '))
comprimento = float(input('Informe o compriment do terreno (em metros): '))
area(largura, comprimento)
